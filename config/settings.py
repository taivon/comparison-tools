def get_oauth_credentials():
    """Fetch OAuth credentials from Secret Manager in production or environment variables in development."""
    if DEBUG:
        # Use environment variables in development
        return {
            "key": os.getenv("GOOGLE_OAUTH2_KEY", ""),
            "secret": os.getenv("GOOGLE_OAUTH2_SECRET", ""),
        }
    else:
        # Fetch from Google Secret Manager in production
        try:
            from google.cloud import secretmanager

            client = secretmanager.SecretManagerServiceClient()

            # Fetch OAuth key
            key_name = "projects/comparison-tools-479102/secrets/google-oauth2-key/versions/latest"
            key_response = client.access_secret_version(request={"name": key_name})
            oauth_key = key_response.payload.data.decode("UTF-8")

            # Debug: Log OAuth key value
            import logging

            logger = logging.getLogger(__name__)
            logger.debug(f"DEBUG_OAUTH_KEY length: {len(oauth_key)}")

            # Fetch OAuth secret
            secret_name = "projects/comparison-tools-479102/secrets/google-oauth2-secret/versions/latest"
            secret_response = client.access_secret_version(request={"name": secret_name})
            oauth_secret = secret_response.payload.data.decode("UTF-8")

            # Debug: Log OAuth secret value
            logger.debug(
                f"DEBUG_OAUTH_SECRET length: {len(oauth_secret)}"
            )  # Validate that credentials are not empty or None
            if not oauth_key or not oauth_key.strip():
                raise ValueError("OAuth key is empty or contains only whitespace")

            if not oauth_secret or not oauth_secret.strip():
                raise ValueError("OAuth secret is empty or contains only whitespace")

            return {"key": oauth_key.strip(), "secret": oauth_secret.strip()}

        except Exception as e:
            import logging

            logger = logging.getLogger(__name__)
            logger.error(f"Failed to fetch OAuth credentials from Secret Manager: {e}")
            # Fallback to environment variables if Secret Manager fails
            return {
                "key": os.getenv("GOOGLE_OAUTH2_KEY", ""),
                "secret": os.getenv("GOOGLE_OAUTH2_SECRET", ""),
            }


